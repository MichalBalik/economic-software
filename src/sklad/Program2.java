    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        double hM ;
        boolean nC ;
        int dB ;
        double dpn ;
        double ostatneZrazky;
        double predavok ;

        try {
            hM = Prevod.parseDouble(this.jTextField1.getText());
            nC = this.jCheckBox1.isSelected();
            dB = this.jSlider1.getValue();
            dpn = Prevod.parseDouble(this.jTextField3.getText());
            ostatneZrazky = Prevod.parseDouble(this.jTextField4.getText());
            predavok = Prevod.parseDouble(this.jTextField5.getText());

        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Skontrolujte udaje ", "Chyba", JOptionPane.ERROR_MESSAGE);
            return;
        }

        CistaMzda cistaMzda2 = new CistaMzda( hM, nC,  dB, dpn,  ostatneZrazky, predavok);
        cistaMzda2.vypocetCistejMzdy();
        this.jTextArea2.setText(cistaMzda2.toString());

    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        double mesacnaMzda;
        int kalendarnyFond;
        int odpracovaneDni;
        double priznanePremie;
        double nahradaMzdy;

        try {
            mesacnaMzda = Prevod.parseDouble(this.jTextField2.getText());
            kalendarnyFond = Integer.parseInt(this.jTextField6.getText());
            odpracovaneDni = Integer.parseInt(this.jTextField7.getText());
            priznanePremie = Prevod.parseDouble(this.jTextField8.getText());
            nahradaMzdy = Prevod.parseDouble(this.jTextField9.getText());

        } catch (ParseException e) {
            JOptionPane.showMessageDialog(this, "Zle zadane udaje ", "Chyba", JOptionPane.ERROR_MESSAGE);
            return;
        }
        MesacnaHrubaMzda lala = new MesacnaHrubaMzda(BigDecimal.valueOf(mesacnaMzda), odpracovaneDni, BigDecimal.valueOf(priznanePremie), BigDecimal.valueOf(nahradaMzdy), kalendarnyFond);
        BigDecimal vysledok = lala.vypocetMesHrubejMzdy();
        this.jTextField15.setText(vysledok.toString());

    }//GEN-LAST:event_jButton2ActionPerformed

