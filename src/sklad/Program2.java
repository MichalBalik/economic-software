    /**
     * Metoda na vypis tabulky s databazov tovarov
     */
    @Override
    public void vypis() {
        this.model.setRowCount(0);
        for (int i = 0; i < this.sklad.getPocetTovarov(); i++) {
            this.model.addRow(new Object[]{this.sklad.vratTovar(i).nazovTovaru(), this.sklad.vratTovar(i).cenaTovaru(),
                this.sklad.vratTovar(i).pocetKusov(), this.sklad.vratTovar(i).datumPridania().toString(),
                this.sklad.vratTovar(i).datumSpotreby().toString()});
        }
        
        this.jTextArea1.setText(this.sklad.toString());
        
        
    
    }

   
    
